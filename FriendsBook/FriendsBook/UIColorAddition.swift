import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var marineBlue: UIColor {
        return UIColor(red: 2.0 / 255.0, green: 63.0 / 255.0, blue: 96.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var greyishBrown: UIColor {
        return UIColor(white: 68.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var white: UIColor {
        return UIColor(white: 249.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var black13: UIColor {
        return UIColor(white: 0.0, alpha: 0.13)
    }
    
    @nonobjc class var brownGrey76: UIColor {
        return UIColor(white: 142.0 / 255.0, alpha: 0.76)
    }
    
    @nonobjc class var fullwhite: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var brownGrey: UIColor {
        return UIColor(white: 155.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var whiteBackground: UIColor {
        return UIColor(white: 251.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var whiteTextField: UIColor {
        return UIColor(white: 235.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var scarlet: UIColor {
        return UIColor(red: 208.0 / 255.0, green: 2.0 / 255.0, blue: 27.0 / 255.0, alpha: 1.0)
    }
    
}
